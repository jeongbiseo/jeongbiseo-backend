package com.jeongbiseo.infra.enrichment.dto;

/**
 * 보강 결과를 활성 저장하지 않은 사유임. 조용히 버리지 않고 사유를 남기는 이유는 두 가지임 — 발표 지표 3개 중 둘(근거 검증 통과율, 기권율)이 이
 * 분포에서 바로 나오고, 검증기가 과하게 조이면 정상 결과까지 폐기되는데 사유별 집계가 없으면 그 사실을 알 수 없기 때문임.
 */
public enum RejectionReason {

	/** JSON이 스키마·enum에 맞지 않거나 파싱되지 않음. */
	SCHEMA_INVALID,

	/** 필드 간 규칙 위반(예 MONTHLY가 아닌데 monthlyAmount가 있음). */
	FIELD_RULE_VIOLATION,

	/**
	 * 근거 문장이 원문에 없음. 환각을 거르는 주 방어선이라 이 사유의 비율이 곧 근거 검증 통과율의 여집합임. 비율이 비정상적으로 높으면 모델이 아니라
	 * 정규화 로직을 먼저 의심할 것(NFC·공백 압축을 빠뜨리면 정상 결과가 대량 폐기됨).
	 */
	EVIDENCE_NOT_IN_SOURCE,

	/** 요청 시점과 저장 직전의 원문 해시가 달라 결과가 낡음. */
	CONTENT_CHANGED,

	/** 지급액이 아닌 금액(대출 한도·사업예산·자부담)을 지급액으로 판정함. */
	POLICY_VIOLATION,

	/**
	 * 근거 문장은 원문에 있으나 모델이 답한 금액이 그 문장의 금액과 다름(자릿수 오독 등). 근거 검사만으로는 잡히지 않는 의미 오류 중 가장 위험한
	 * 축이라 별도 사유로 둠 — 이 비율이 높으면 프롬프트의 단위 환산 지시를 손봐야 함.
	 */
	AMOUNT_NOT_IN_EVIDENCE,

	/** 모델이 스스로 기권함. 실패가 아니라 정상 동작이며 기존 산정불가 상태를 유지함. */
	ABSTAINED

}
