package com.jeongbiseo.infra.enrichment.dto;

/**
 * LLM이 판정한 지급 주기임. 기존 {@link com.jeongbiseo.domain.common.enums.PaymentType}과 축이 다름 — 그쪽은
 * 지급 수단(현금·바우처·현물·감면)이고 이쪽은 지급 빈도임. "월 20만원을 12개월"처럼 수단은 현금이면서 주기는 월인 조합이 실재하므로 한 enum으로
 * 합치지 않음.
 *
 * 이 판정이 필요한 이유는 실측 때문임 — 파서가 '원'으로 읽은 78건 중 17건이 원문상 월 단위였고(안양 청년 월세 지원 = 월 20만원 곱하기
 * 12개월), 단위를 모르면 총액을 만들 수 없음(판정원칙 7번).
 */
public enum PaymentPeriod {

	/** 한 번에 지급함. */
	LUMP_SUM,

	/** 매월 지급함. durationMonths와 짝을 이룸. */
	MONTHLY,

	/** 매년 지급함. */
	ANNUAL,

	/** "자녀 1명당", "ha 당"처럼 단위마다 지급함. 총액은 단위 수를 알아야 정해지므로 합산 대상이 아님. */
	PER_UNIT,

	/**
	 * 원문으로 주기를 정할 수 없음. 참전유공자수당 같은 종신 지급도 여기 넣음 — 총액 개념 자체가 없어 임의 기간으로 환산하면 안 됨(판정원칙 3번의
	 * "종신 수당을 임의 지급 기간으로 환산" 금지).
	 */
	UNKNOWN

}
