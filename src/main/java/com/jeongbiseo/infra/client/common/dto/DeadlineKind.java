package com.jeongbiseo.infra.client.common.dto;

/**
 * 신청기한을 의미 단위로 분류한 8종임(임무 지시 1장 — "상시접수는 파싱 실패가 아니라 명확한 의미다"). 기존
 * {@link com.jeongbiseo.infra.client.gov24.dto.DeadlineParseResult}의 성공/실패 이분법을 대체하지 않고
 * 병행함(기존 테스트가 이 이분법에 의존하므로 유지 — 임무 지시 6장). 아래 각 값에 붙은 스냅샷(n=1097) 실측 분포는 <b>gov24 신청기한
 * 자유텍스트 기준</b>이며, 상세는
 * {@link com.jeongbiseo.infra.client.gov24.Gov24Parser#classifyDeadlineKind(String)}
 * Javadoc 참조.
 *
 * <p>
 * 4종 소스 공통 타깃으로 승격함(2026-07-12). 이 분류는 소스와 무관한 의미 체계라 그대로 재사용하되, <b>분류 방법은 소스마다 다름</b> —
 * gov24와 기업마당은 텍스트에서 판정하고(파싱), 온통청년과 K-Startup은 구조화 필드를 그대로 매핑함(파싱 아님). 이 차이는
 * {@link DeadlineBasis}에 기록함.
 */
public enum DeadlineKind {

	// 단일 마감일 — "YYYY년 M월 D일까지" 같은 절대 연월일 1개. 스냅샷 실측 0건(신청기한 필드는 전부 범위 형식이거나
	// 키워드형이었음 — 다른 소스(K-Startup 등)를 위해 분류 자체는 유지함)
	FIXED_DATE,

	// 시작일과 종료일이 함께 있는 범위 — "YYYY.MM.DD~YYYY.MM.DD"(둘째 날짜가 연도를 생략하면 첫째 날짜 연도를
	// 물려받음). 스냅샷 실측 51건(4.65%)
	DATE_RANGE,

	// 상시접수 — "상시신청"·"연중" 등. 마감 개념 자체가 없는 정상 상태임(실패 아님). 스냅샷 실측 673건(61.35%)
	ALWAYS_OPEN,

	// 정기·주기적 — "매년"·"반기"·"분기"·"매월" 등 반복 주기가 원문에 명시됨. 스냅샷 실측 79건(7.20%)
	PERIODIC,

	// 예산 소진 시까지 — "소진" 키워드. 고정 날짜가 없고 예산 상황에 마감이 달려 있음. 스냅샷 실측 11건(1.00%)
	UNTIL_BUDGET_EXHAUSTED,

	// 별도 공고에 따름 — "공고"·"공모"·"모집"·"규정에 따름" 등, 확정 시점 대신 별도 공고 참조를 안내함. 스냅샷 실측
	// 37건(3.37%)
	ANNOUNCEMENT_BASED,

	// 접수가 이미 끝남 — 이 값은 4종 소스 통합 회차(2026-07-12)에 추가함. gov24 스냅샷 실측 0건이고 gov24
	// 파서는 이 값을 만들지 않음(자유텍스트에 "마감됨"을 선언하는 어휘가 없음). 추가한 이유는 **온통청년이 이 상태를
	// 코드로 선언**하기 때문임 — aplyPrdSeCd=0057003(마감)이 전수 2,648건 중 889건(33.6%)이고 그 889건은
	// aplyYmd가 전부 공백임(조사 리포트 주3 크로스탭, 위반 0건). 이 값이 없으면 온통청년 3분의 1이 UNKNOWN으로
	// 뭉개져 "마감된 것"과 "모르는 것"이 구분되지 않음 — 캘린더·추천 모집단에서 정반대로 다뤄야 하는 둘을 섞는 셈이라
	// 별도 값으로 둠(함정 4 "성공과 실패의 이분법을 버릴 것"과 같은 성격)
	CLOSED,

	// 위 7종 어디에도 걸리지 않음 — "접수기관 별 상이"처럼 근거가 있는 값도 있고, 상대적 기한("전입일로부터 3개월
	// 이내")처럼 이 enum이 표현 못 하는 값도 있음. 근거 없는 분류를 지어내지 않고 정직하게 남김(임무 지시 1장 "상상
	// 금지"). 스냅샷 실측 246건(22.42%)
	UNKNOWN

}
