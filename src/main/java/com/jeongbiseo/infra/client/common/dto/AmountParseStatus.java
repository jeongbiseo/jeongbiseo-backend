package com.jeongbiseo.infra.client.common.dto;

/**
 * 금액 추출 시도 자체의 성공 여부임. {@link AmountKind#NONE}과 짝을 이루는 필드로, "금액 표현이 없어서 못 찾음(정상 상태)"과
 * "찾았지만 값이 이상함" 같은 향후 세분화를 구분하려고 별도 필드로 둠.
 */
public enum AmountParseStatus {

	// 금액 표현을 1개 이상 찾아 값까지 계산함
	PARSED,

	// 지원내용 원문에 금액 표현이 없거나(정상 상태, 예 서비스·현물형) 원문 자체가 비어 있음. 스냅샷 실측 645건
	NOT_FOUND,

	// 금액 표현은 있었으나 전부 사업예산·총사업비·상금 총액 같은 예산 문맥이라 개인 지급액 후보에서 배제함(오분류
	// 수정 임무 유형A). "조용히 버리지 말고 별도 상태로 남겨라"는 지시에 따라 NOT_FOUND와 구분함 — 이 상태는
	// 원문에 금액이 아예 없던 게 아니라, 있었지만 신뢰할 수 없어 걸러냈다는 뜻임(팀이 엑셀에서 원문을 직접
	// 확인할 대상). amountKind는 NONE이 됨. 스냅샷 실측 6건(154300000061, 430000000135,
	// 519000000102, 524000000122, 559000000440, 650000000330)
	EXCLUDED_BUDGET_CONTEXT,

	// 예산 문맥 금액을 일부 배제했지만 개인 지급액 후보가 하나 이상 남아 정상 파싱된 경우임. 배제 사실을
	// EXCLUDED_BUDGET_CONTEXT처럼 드러내되(조용히 버리지 않음) 금액은 살아 있으므로 PARSED와도 구분함 —
	// 이 상태의 레코드는 "배제 판정이 맞았는지" 팀이 원문으로 재확인할 대상임. gov24 스냅샷 실측 6건
	// (155000000018, 338000000419, 402000000148, 452000000326, O00081200001,
	// SD0000010824).
	//
	// 2026-07-12 적대 검증(과잉 배제 최우선)으로 **단위 배분액 되살리기**를 넣으면서 온통청년 2건이 이 상태로 옮겨
	// 왔음 — 20250901005400211556("1인당 사업비 12백만원")과 20250109005400210105("개소당 사업비
	// 30백만원")은 예산 어휘가 붙었다는 이유로 EXCLUDED_BUDGET_CONTEXT(산정불가)였으나, 실제로는 수혜자
	// 1단위가 받는 지급액이라 금액을 되살리고 CONDITIONAL로만 강등함. 두 건 모두 같은 문장의 진짜 사업 총예산
	// (30백만원·300백만원)은 그대로 배제돼 이 상태가 됨(판별 근거는 Gov24Parser#isPerUnitBudgetAllocation).
	PARSED_WITH_BUDGET_EXCLUSION,

	// 금액 표현은 있었으나 전부 이용자 자부담금(수혜자가 **내는** 돈)이라 지급액 후보에서 배제함(오분류 수정
	// 임무 유형G, 2026-07-12 적대 검증 High). 예산 문맥과 방향이 정반대라(예산은 정부가 쓰는 돈, 자부담은
	// 이용자가 내는 돈) 사유를 섞지 않고 별도 상태로 둠 — 팀이 엑셀에서 "왜 산정불가인가"를 바로 읽을 수 있어야
	// 하기 때문임. amountKind는 NONE이 됨. 스냅샷 실측 2건(304000000150 "학생 자부담금 : 10,000원",
	// 383000000172 "자부담금 : 19만원 내외" — 둘 다 실제 보조금은 "가입비 일부"·"설치금액의 80%"처럼
	// 비율로만 적혀 금액이 없음)
	EXCLUDED_SELF_PAY_CONTEXT,

	// 자부담금을 일부 배제했지만 지급액 후보가 하나 이상 남은 경우임. 이 상태의 레코드는 자부담(매칭 펀드) 구조가
	// 있다는 뜻이라 남은 금액이 순 지급액이 아닐 수 있어(예 483000000113 "지원단가 20,000천원 = 보조
	// 10,000천원 더하기 자부담 10,000천원") amountKind를 CONDITIONAL로 강등함 — 예상총액 자동 채움 대상에서
	// 빼되 금액은 보존함. 스냅샷 실측 5건(392000000164, 483000000113, 516000000123,
	// 571000000120, 650000001099)
	PARSED_WITH_SELF_PAY_EXCLUSION,

	// 금액 표현은 있었으나 전부 대출·융자·보증 **한도**(빌릴 수 있는 상한이나 보증 잔액)라 지급액 후보에서 배제함
	// (금액 오분류 수정 임무 유형H, 2026-07-12). 앞선 두 사유와 또 방향이 다름 — 예산은 정부가 쓰는 돈,
	// 자부담은 이용자가 내는 돈, **대출 한도는 애초에 아무도 주지 않는 돈**(빌린 뒤 갚아야 하는 채무 상한)임.
	// 그래서 사유를 섞지 않고 별도 상태로 둠. amountKind는 NONE이 됨.
	//
	// 핵심 함정은 "대출"이라는 낱말이 아니라 **그 금액이 대출 원금·한도인지 지원받는 금액인지**임. "대출이자 지원
	// (최대 100만원)"의 100만원은 진짜 지급액이라 남기고, "대출한도 : 최대 1억원"의 1억원만 배제함(판별 근거는
	// Gov24Parser#isLoanLimitAmount Javadoc). 스냅샷 실측 8건 — 20250502005400210779(부산청년
	// 머물자리론 "대출최대한도 : 1억 원"), 20250502005400210775(신혼부부 주거자금 "대출잔액 1억원 한도 내"),
	// 20250502005400210772(청년 주택 임차보증금 "대출한도 : 최대 1억원"), 569000000379, 300000000165,
	// 142100000071, B55370100029(경남동행론 "대출한도 : 최대 300만원"), B55370100042
	EXCLUDED_LOAN_CONTEXT,

	// 대출·보증 한도를 일부 배제했지만 지급액 후보가 하나 이상 남은 경우임. 자부담과 같은 이유로 남은 금액의
	// amountKind를 CONDITIONAL로 강등함 — 배제가 조건 마커까지 함께 걷어내 남은 금액이 SINGLE로 **승격**되면
	// 없던 예상총액 항목이 새로 생기기 때문임(451000000242 실측 — "대출한도 5천만 원"을 빼면 뒤의 "연 최대
	// 1.5백만 원"이 MULTIPLE에서 SINGLE로 올라섬). 금액 자체는 보존하므로 과잉 배제가 아님. 스냅샷 실측 15건
	// (142000000098, 451000000242, 626000000647, 644000000860, B55369400017,
	// B55400900047,
	// 20260406005400212462, 20260318005400212197, 20260326005400212292,
	// 20260422005400212850,
	// 20251217005400212015, 20251218005400212025, 20250917005400211733,
	// 20250407005400210696,
	// 20250123005400110377)
	PARSED_WITH_LOAN_EXCLUSION,

	// 금액 표현은 있었으나 "소득" 어휘 앞 문맥과 "이하·미만·이상·초과" 뒤 문턱 마커가 함께 있어 지급액이
	// 아닌 자격 기준선으로 배제한 경우임. 표본 2,421건에서 이 조합 12건은 전부 자격 기준선이고 실제 지급액은
	// 0건이었음(DEC-019). amountKind는 NONE이 됨.
	EXCLUDED_INCOME_THRESHOLD,

	// 소득 자격 기준선을 일부 배제했지만 실제 지급액 후보가 하나 이상 남은 경우임. 배제 후 하나만 남더라도
	// SINGLE로 승격하지 않고 CONDITIONAL로 유지해 예상 총액 오합산을 막음(20250901005400211565 실측).
	PARSED_WITH_INCOME_THRESHOLD_EXCLUSION

}
