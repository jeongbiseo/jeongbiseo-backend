package com.jeongbiseo.infra.client.common.dto;

/**
 * 지원내용 원문에서 뽑은 금액 표현의 성격임(임무 지시 2장). MULTIPLE과 CONDITIONAL을 가르는 것이 핵심 — 둘 다 숫자가 2개 이상일 수
 * 있지만, CONDITIONAL은 "자녀 1명당 100만원"처럼 조건 표현(당·별·이상·이하·경우·시·기준)이 금액 근처에 붙어 있어 단순 합산하면 안 됨을
 * 뜻함. 스냅샷(n=1097) 실측 분포는
 * {@link com.jeongbiseo.infra.client.gov24.Gov24Parser#parseAmount(String)} Javadoc 참조.
 */
public enum AmountKind {

	// 지원내용 원문에 금액 표현(숫자+원 단위)이 전혀 없음. 스냅샷 실측 645건(58.80%, 현금성 433건 기준
	// 154건/35.57%)
	NONE,

	// 금액이 정확히 1개이고 조건 표현이 근처에 없음 — 금액 표현 축에서는 예상총액 합산에 가장 안전한 경우. 스냅샷
	// 실측 146건(13.31%, 현금성 기준 95건/21.94%).
	//
	// **SINGLE + CASH를 곧바로 예상총액에 합산하면 안 됨(2026-07-12 적대 검증).** 이 분류는 "금액 표현이
	// 단일하고 조건이 안 붙었다"만 뜻하지 "그 돈을 개인이 받는다"를 뜻하지 않음. 4종 6,840건에서 SINGLE +
	// CASH이면서 1천만원 이상인 9건 중 5건이 **기업이 받는 돈**임(B55101400003 창업기업 지원금 5,500만원,
	// 142000000061 중소기업 시스템 구축 4,000만원, 20260416005400112760 창업기업 해외진출자금 3,000만원,
	// 20260330005400212309 청년 기업 창업 1,000만원, 20251107005400211811 고용환경개선 1,000만원).
	// 수혜 단위(개인 대 기업)는 금액 표현으로 갈리지 않아 지원대상 필드를 공통 타깃으로 승격해야 풀리며, 그
	// 전까지 예상총액 자동 합산은 출시하지 않음(빌드 게이트 — EstimatedTotalGateTest).
	SINGLE,

	// 금액이 2개 이상이되 조건 표현이 근처에 없는 단순 나열(예 국공립·사립처럼 대상별로 다른 금액을 병기했지만
	// "당"·"별" 같은 조건 접속이 없는 경우). 스냅샷 실측 103건(9.39%, 현금성 기준 55건/12.70%)
	MULTIPLE,

	// 조건별 차등 지급 — "자녀 1명당 100만원", "ha 당 50~600만원"처럼 조건 표현이 금액 바로 앞에 붙어 있어 단순
	// 합산이 위험함(조건에 따라 배수가 달라짐). 스냅샷 실측 203건(18.51%, 현금성 기준 129건/29.79%)
	CONDITIONAL

}
