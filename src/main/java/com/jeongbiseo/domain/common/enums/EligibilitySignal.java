package com.jeongbiseo.domain.common.enums;

/**
 * 소득과 가구 조건 플래그를 3분류로 정규화한 신호임(외부API-부족분-조사-2026-07-12.md 3장 정규화 규칙, 전수 10,968건 실측으로 재현
 * 확인됨, 소득 유효 90.61%, 제한형 19.98%, 가구 유효 90.75%, 제한형 11.75%). 소득 5구간(JA0201~JA0205) 또는 가구
 * 9필드(JA0401~JA0414)를 원문 그대로 읽은 뒤 이 신호로 계산함.
 *
 * <p>
 * 이 신호를 매칭 하드 필터로 쓸지 소프트 랭킹 신호로만 쓸지는 회의 안건 22번 결정 사항이라 이 enum 자체는 필터 채택 여부를 규정하지 않음. 적대
 * 검증에서 플래그가 선정기준 원문과 어긋나는 사례가 실측 확인됐으므로 (예: 한부모 무료법률구조는 원문 중위소득 125% 이하인데 플래그는 75%까지만 Y,
 * 과소 커버) 하드 배제에 쓸 때는 원문 대조가 필요함.
 */
public enum EligibilitySignal {

	// 해당 조건이 명시적으로 전부 열려 있음(무관). 소득은 5구간 전부 Y, 가구는 실질 8필드(JA0410 제외) 전부
	// Y이거나 실질 8필드가 전부 null이면서 JA0410(해당사항없음)만 단독으로 Y인 경우
	UNRESTRICTED,

	// 조건 중 일부만 Y. 특정 소득구간·가구유형만 대상임(제한형). 원문 선정기준과 항상 일치하지는 않음
	// (외부API-부족분-조사-2026-07-12.md 7장 C1, 적대 검증 대조 표본에서 불일치 사례 확인)
	RESTRICTED,

	// 관련 필드가 전부 null. 원문 자체에 신호가 없음(데이터없음). "제한없음"과 반드시 구분해야 함
	UNKNOWN

}
