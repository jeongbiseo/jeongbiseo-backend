package com.jeongbiseo.domain.common.enums;

/**
 * 지원금을 <b>받는 주체</b>가 개인인지 사업자인지임. 개인 대상 현금성 지원금만 추천과 합산한다는 제품 범위(2026-07-13 확정)를 데이터로
 * 강제하는 축임.
 *
 * <p>
 * <b>gov24는 이 값을 원천에서 준다.</b> {@code serviceList} 오퍼레이션의 {@code 사용자구분} 필드이며 전수 10,974건에
 * 채워져 있음(2026-07-13 실측). 값 도메인은 개인 82.0%, 법인/시설/단체 6.6%, 가구 3.9%, 소상공인 2.1%와 이들의
 * {@code ||} 조합임. <b>{@code serviceDetail}에는 이 필드가 없어서</b> 상세만 읽던 파서가 그동안 놓쳤음. 새 소스를 조사할
 * 때 오퍼레이션별 응답 키 합집합을 세야 하는 이유임.
 *
 * <p>
 * <b>이 축은 자격조건이 아니라 스코프임.</b> 농업인 전용 지원금은 개인이 받는 돈이라 {@link #PERSONAL}이 맞고, 추천에서 빠지는 것은
 * 별개 축({@link OccupationRestriction})이 판정함. 두 축을 합치면 "농업인 전용은 개인 대상이 아니다"라는 거짓 명제를 데이터
 * 모델에 박게 됨.
 */
public enum TargetAudience {

	// 개인 또는 가구 신호만 있음. 추천과 합산 모두 포함함
	PERSONAL,

	// 사업자 신호만 있음(소상공인과 법인/시설/단체). 추천과 합산에서 제외하되 레코드는 보존함. 결과가 0건이지 데이터가 0건이 아님
	BUSINESS,

	// 개인 신호와 사업자 신호가 함께 있음(gov24 "개인||소상공인" 등 전수 287건, 2.6%). 추천에는 넣고 합산에서만 뺌.
	// 전략작물직불처럼 개인이 실제로 받는 지원금이 여기 있어서 전면 배제하면 함께 죽음. 금액 오염만 막으면 됨
	MIXED,

	// 판정 근거가 없음. 추천은 통과시킴("조건을 모르면 탈락시키지 말고 통과시킨 뒤 사유를 붙인다"는 추천 도메인 불변식),
	// 합산은 PERSONAL만 허용하므로 자동으로 빠짐
	UNKNOWN

}
