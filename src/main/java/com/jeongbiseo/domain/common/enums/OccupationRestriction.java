package com.jeongbiseo.domain.common.enums;

/**
 * 지원금이 특정 직업군 전용인지임. <b>제품 타깃(20대와 30대 청년·사회초년생·취업준비생) 밖의 지원금을 추천 스코프에서 자르는 축</b>임.
 *
 * <p>
 * <b>이것은 프로필 매칭이 아니라 스코프 정책임.</b> {@code ApplicantProfile}에 농·어·축·임업 여부가 없으므로 "프로필과 대조해
 * 불일치"가 아니라 "제품이 그 신분을 선언할 수단 자체를 제공하지 않는다"는 결정임. 그래서 장애인·보훈·질병 전용과 모순되지 않음. <b>장애인 청년은
 * 우리 타깃 사용자일 수 있어 포용하고, 1차산업 전용은 타깃 정의 밖이라 자름.</b> 같은 축의 비대칭이 아니라 종류가 다른 결정임.
 *
 * <p>
 * <b>판정 근거는 gov24 {@code supportConditions}의 JA0301~JA0330 17개 플래그임.</b> 켜진 집합 S가 비어 있지
 * 않고 S가 1차산업 4종(JA0313 농업인, JA0314 어업인, JA0315 축산업인, JA0316 임업인)의 부분집합일 때만
 * {@link #PRIMARY_INDUSTRY_ONLY}임.
 *
 * <p>
 * <b>JA0322(해당사항없음) 분기를 두지 않은 이유</b>: 잉여임. JA0322가 켜져 있으면 그 순간 S는 1차산업의 부분집합이 아니게 되어 자동으로
 * 통과함. 분기를 지워도 결과가 같음. 참고로 JA0322를 "제한"으로 오독하면 유아학비(누리과정, JA0322 단독)가 죽음.
 *
 * <p>
 * gov24 외 3종 소스는 JA 플래그를 주지 않으므로 전부 {@link #NONE}임(판정 불가는 통과시킴). 따라서 온통청년의 청년 농창업 지원은 이
 * 판정의 대상이 아님.
 */
public enum OccupationRestriction {

	// 직업군 제한이 없거나, 있어도 1차산업 전용이 아님. 추천 스코프 통과함. gov24 외 소스는 판정 근거가 없어 전부 이 값임
	NONE,

	// 농림축수산업 종사자 전용임(gov24 표본 1,097건 중 177건). 제품 타깃 밖이라 추천 스코프에서 제외하되 레코드는 보존함.
	// 향후 1차산업을 다시 포함하기로 하면 재파싱 없이 필터만 풀면 됨
	PRIMARY_INDUSTRY_ONLY

}
